// PRIMITIVE DATATYPES:
// These are the basic datatypes in javascript. They are immutable,meaning their values cannot be changed.

// 1. Number: represents numeric values,eg: 10,3.14
// 2. String: represents text, eg, "hello", 'hello'
// 3. Boolean: represents true or false values
// 4. Null: represents a null or empty value
// 5. Undefined: represents an undefined value
// 6. Symbol: represents a unique symbol (introduced in E6)
// 7. BigInt: represents large integers(introduced in ES11)